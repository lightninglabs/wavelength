package gen

import (
	"bytes"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
)

// Generate generates mailbox RPC stubs for all services in the protoc request.
func Generate(plugin *protogen.Plugin, cfg Config) error {
	for _, file := range plugin.Files {
		if !file.Generate {
			continue
		}

		if len(file.Services) == 0 {
			continue
		}

		if err := generateFile(plugin, file, cfg); err != nil {
			return err
		}
	}

	return nil
}

// generateFile generates RPC-over-mailbox stubs for a single protobuf file.
func generateFile(plugin *protogen.Plugin, file *protogen.File,
	cfg Config) error {

	if !shouldGenerateFile(file, cfg) {
		return nil
	}

	filename := file.GeneratedFilenamePrefix + "_mailboxrpc.pb.go"
	g := plugin.NewGeneratedFile(filename, file.GoImportPath)

	g.P("// Code generated by protoc-gen-mailboxrpc. DO NOT EDIT.")
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()

	for _, svc := range file.Services {
		serviceFQN := protoServiceFQN(file, svc)
		excluded := cfg.ExcludeService != "" &&
			serviceFQN == cfg.ExcludeService
		if excluded {
			continue
		}

		if err := generateService(g, svc, serviceFQN); err != nil {
			return err
		}
	}

	return nil
}

// shouldGenerateFile returns true if file contains at least one non-excluded
// service for which mailbox RPC stubs should be generated.
func shouldGenerateFile(file *protogen.File, cfg Config) bool {
	for _, svc := range file.Services {
		serviceFQN := protoServiceFQN(file, svc)
		excluded := cfg.ExcludeService != "" &&
			serviceFQN == cfg.ExcludeService
		if excluded {
			continue
		}

		return true
	}

	return false
}

// generateService generates typed mailbox RPC stubs for a single service by
// populating template data and executing the service template into a buffer
// before writing to g.
func generateService(g *protogen.GeneratedFile, svc *protogen.Service,
	serviceFQN string) error {

	data := buildServiceData(g, svc, serviceFQN)

	var buf bytes.Buffer
	if err := serviceTmpl.Execute(&buf, data); err != nil {
		return err
	}

	_, err := g.Write(buf.Bytes())

	return err
}

// resolveIdent registers an import with g and returns the qualified Go
// identifier string for use in templates.
func resolveIdent(g *protogen.GeneratedFile, path, name string) string {
	return g.QualifiedGoIdent(protogen.GoIdent{
		GoImportPath: protogen.GoImportPath(path),
		GoName:       name,
	})
}

// buildServiceData constructs template data for a protobuf service. It calls
// g.QualifiedGoIdent for each external type to register the necessary imports.
func buildServiceData(g *protogen.GeneratedFile, svc *protogen.Service,
	serviceFQN string) serviceData {

	const mailboxrpcPath = "github.com/lightninglabs/wavelength/" +
		"mailbox/rpc"
	const protoPath = "google.golang.org/protobuf/proto"

	data := serviceData{
		ServiceName: svc.GoName,
		ServiceFQN:  serviceFQN,
		RPCClient:   resolveIdent(g, mailboxrpcPath, "RPCClient"),
		RPCOptions:  resolveIdent(g, mailboxrpcPath, "RPCOptions"),
		ServiceMethod: resolveIdent(
			g, mailboxrpcPath, "ServiceMethod",
		),
		SendResult:   resolveIdent(g, mailboxrpcPath, "SendResult"),
		Router:       resolveIdent(g, mailboxrpcPath, "Router"),
		Context:      resolveIdent(g, "context", "Context"),
		ProtoMessage: resolveIdent(g, protoPath, "Message"),
		FmtErrorf:    resolveIdent(g, "fmt", "Errorf"),
	}

	for _, m := range svc.Methods {
		data.Methods = append(data.Methods, methodData{
			Name:      m.GoName,
			ProtoName: string(m.Desc.Name()),
			ReqType:   g.QualifiedGoIdent(m.Input.GoIdent),
			RespType:  g.QualifiedGoIdent(m.Output.GoIdent),
		})
	}

	return data
}

// protoServiceFQN returns "<proto package>.<ServiceName>" for a service.
func protoServiceFQN(file *protogen.File, svc *protogen.Service) string {
	pkg := string(file.Desc.Package())
	if pkg == "" {
		return string(svc.Desc.Name())
	}

	return strings.Join([]string{pkg, string(svc.Desc.Name())}, ".")
}
